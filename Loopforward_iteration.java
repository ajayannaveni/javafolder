class Loopforward_iteration
{
 public static void main(String args[])
 {
   int start=1;
   System.out.println("forward");
   while(start<=10)
    {
     System.out.println(start);
     start++;
	
    }
   int start1=10;
	System.out.println("backward");
	while(start1>=1)
	{
	System.out.println(start1);
	start1--;
	}

 }
}